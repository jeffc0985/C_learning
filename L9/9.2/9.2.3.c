//三維陣列
#include <stdio.h>

int main()
{
    int A[2][4][3] = {{{98,44,32},{54,73,58},{10,12,65},{100,44,2}},
                        {{77,96,15},{32,33,41},{24,10,61},{204,8,157}}};
    int i, j, k, max = A[0][0][0];
 
    for(i=0;i<2;i++)
        for(j=0;j<4;j++)
            for(k=0;k<3;k++)
                if(A[i][j][k]>max)
                    max = A[i][j][k];

    printf("三維陣列A中最大的元素為:%d",max);
    return 0;
}
//2*4*3的三維陣列可看成2個4*3的二維陣列